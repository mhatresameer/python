

"""
What does ** do? - the ** operator is a power to any operator.
"""

user_power_property = int(input("Enter any number to check the power: "))
power_raised = int(input("Enter the power to be used with the given number: "))
result_power_property = user_power_property ** power_raised
print(f"The number {user_power_property} with power raised to {power_raised} is {result_power_property}")

# What does ** do? - 2^4 which means the 2 raise to the power of 4
