# Level 3: String Data Type
""" Take users input and print it in uppercase, lowercase and titlecase """

user_name = input("Enter your username to check it in uppercase, lowercase and titlecase: ")
uppercase_username = user_name.upper()
lowercase_username = user_name.lower()
titlecase_username = user_name.title()

print(uppercase_username)
print(lowercase_username)
print(titlecase_username)
