# Level 7: Sets
"""
Predict output:
nums = {1, 2, 2, 3, 3, 4}
print(nums)
"""

nums = {1, 2, 2, 3, 3, 4}
print(nums)
# O/P: {1, 2, 3, 4}
# When you create a set with duplicate values, Python automatically removes the duplicates, so the set will only contain 1, 2, 3, 4.
# The print(nums) statement then displays this set to the console.
