# a variables value can be changes at the moment of assigning it. Which means it doesnt hold any types.

# case-sensetive - Python is case sensetive and below both scenarios are different variable names.
username = "username is one variable in case-sensetive."
Username = "Username is a different variable in case-sensetive."

print(Username)

# declare a variable name in Python

# CAMEL CASE
