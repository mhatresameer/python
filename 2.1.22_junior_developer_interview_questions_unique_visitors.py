"""
You have:
visitors = [
   "Sam",
   "John",
   "Sam",
   "Alice",
   "John"
]
Find unique visitors using a set.
"""
# the given data-type is LIST, we need to make sure the data-type is SET and the SET has a feature to hold only unique values.
visitors = [
   "Sam",
   "John",
   "Sam",
   "Alice",
   "John"
]
print(type(visitors))
print(visitors)

# this is SET.
visitors = {
   "Sam",
   "John",
   "Sam",
   "Alice",
   "John"
}

print(type(visitors))
print(visitors)
